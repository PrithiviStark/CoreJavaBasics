package com.qspiders.basics;

public class PrimitivesVariables
{
public static void main(String[] args)
{/*primitives are inbiult datatypes ,there are 8 primitives in java*/
	byte a=1;
	short b=10;
	int c=100;
	long d=1000;
	float e=10.0f;
	double f=100.0;
	char g='a';
	boolean h=true;
	final int i=20;
	final float j=20.0f;
	final char k='b';
	final boolean l=false;
	System.out.println("values are"+a+b+c+d+e+f+g+h+i+j+k+l);
}
}
