package com.qspiders.basics;

public class UnaryOperator 
{
public static void main(String[] args) 
{
int a=10;
int b=20;
System.out.println(a++);//post increment operation
System.out.println(a);
System.out.println(++a);//pre-increment operation
System.out.println(b--);//post decrement operation
System.out.println(b);
System.out.println(--b);//pre-decrement operation
}
}
