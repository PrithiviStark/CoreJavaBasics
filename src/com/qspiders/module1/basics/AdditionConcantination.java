public class AdditionConcantination
{
public static void main(String[] args) {
	System.out.println(20+20);
	System.out.println(20+"numbers");
	System.out.println(20+20+"numbers");
	System.out.println("numbers"+20);
	System.out.println("numbers"+20+20);
	System.out.println(20+20+"numbers"+20+20);
}
}
